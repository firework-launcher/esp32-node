def generateOutput(digit1, digit2, digit3, arm):
    output = list('0000000000000000')
    def appendToOutput(data):
        y = 0
        for x in data:
            if output[y] == '0' and x == 1:
                output[y] = '1'
            y += 1
    appendToOutput(numbers[0][int(digit1)])
    appendToOutput(numbers[1][int(digit2)])
    appendToOutput(numbers[2][int(digit3)])
    if arm:
        appendToOutput(arm_data)
    return ''.join(output)

